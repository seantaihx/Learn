#Numeric Data Types
'''
Numbers: Numbers without quotation marks

1. Integer: Whole number without decimals
ex: 1, 56, 48723278427

2. Float: Numbers with decimals
ex: 1.0, 423.2143, 213.3413

3. Complex: Numbers with real part and imaginary part
ex: 3+2j, 1+2j
'''



#String
'''
String: Everything inside quotation marks, double or single is fine
ex: '1', "ufwfueni", "342"

!!!Everything from input is string
'''



#Boolean
'''
Boolean: True or False
ex: True, False

!!Note the capital letter
'''



#Data Type Checking
'''
type(): Check the type of a value

format: type(value)             --> This is not printed, so we usually accompany this with print()
ex: print(type(1))

or
isinstance(): Check if a value has certain type

format: isinstance(value/variable, type)        --> Returns True if match and False if not match
ex: isinstance(3, int)
'''



#Data Type Conversion
'''
int(): change to integer
float(): change to float
str(): change to string
bool(): change to boolean

!!! If change the type of non-numeric data into a data type, it will result in error
'''
