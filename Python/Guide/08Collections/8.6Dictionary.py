#Dictionaries
'''
Dictionaries: Sortable, Mutable, NOT allow duplicates, Can store different data types

format: dictionary_name = {key1: value1, key2: value2, key3: value3, ...}
!!!Always come in key value pairs
!!!Key: Value pairs are called items
'''


#Create a dictionary
'''
1. dictionary_name = {key1: value1, key2: value2, ...}
2. dictionary_name = dict(key1 = value1, key2 = value2, ..)
'''

#Checking the type of dictionary
'''
format: type(dictionary_name)
'''

#Accessing
'''
To get value:
1. format: dictionary_name[key]
2. format: dictionary_name.get(key)

To get all values:
1. format: dictionary_name.values()

To get key:
1. format: dictionary_name.keys()

To get key-value pairs:
1. format: dictionary_name.items()
'''

